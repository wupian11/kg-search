# 简体转繁体映射（反向映射）
SIMP_TO_TRAD: dict[str, str] = {v: k for k, v in TRAD_TO_SIMP.items()}


def to_simplified(text: str) -> str:
    """
    繁体转简体

    Args:
        text: 输入文本

    Returns:
        转换后的简体文本
    """
    if not text:
        return text

    result = text
    for trad, simp in TRAD_TO_SIMP.items():
        result = result.replace(trad, simp)
    return result


def to_traditional(text: str) -> str:
    """
    简体转繁体

    注意：此方法基于字符映射，可能不完全准确（一简对多繁的情况）

    Args:
        text: 输入文本

    Returns:
        转换后的繁体文本
    """
    if not text:
        return text

    result = text
    for simp, trad in SIMP_TO_TRAD.items():
        result = result.replace(simp, trad)
    return result


def normalize_text(text: str, to_traditional_chinese: bool = True) -> str:
    """
    标准化文本（繁简转换）

    Args:
        text: 输入文本
        to_traditional_chinese: True 转繁体，False 转简体

    Returns:
        标准化后的文本
    """
    if to_traditional_chinese:
        return to_traditional(text)
    return to_simplified(text)
